const mongoose = require("mongoose");

// Mongoose generates ID automically by defaul
const UserSchema = mongoose.Schema({
  country: {
    type: String,
    required: true,
  },
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  address: {
    type: String,
    required: true,
  },
  city: {
    type: String,
    required: true,
  },
  state: {
    type: String,
    required: true,
  },
  zipCode: String,
  phoneNumber: String,
});

module.exports = mongoose.model("users", UserSchema);
